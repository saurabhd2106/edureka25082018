package day2;

public class VariableExamples {
	
	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int firstNumber;
		
		firstNumber = 23;
		
		int secondNumber = 78;
	
		System.out.println(secondNumber);
		System.out.println(secondNumber++);
		System.out.println(++secondNumber);
		
		System.out.println("------------------------------------");
		int sum = firstNumber + secondNumber;
		
		System.out.println("Sum : "+ sum);
		
		System.out.println("First Number : "+ firstNumber+ secondNumber);
		
		System.out.println("Sum of two numbers Number : "+ (firstNumber+ secondNumber));
		
		byte myFirstByteNum = 70;
		
		System.out.println("Byte number : "+ myFirstByteNum);
		
		long longNumber = 729542757341857l;
		
		float floatVar = 78.904f;
		
		double dobNum = 4986.858935;
		
		boolean flag = false;
		
		char myFirstChar = '%';
		
		String firstString = "My name is saurabh";
		
		System.out.println(firstString);
	}

}
